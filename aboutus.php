		  <h4>About Our Business</h4>
		 <p> Indus Motor Company Limited (IMC) was incorporated in 1989 as a result of a joint venture agreement 
		 among some companies of House of Habib of Pakistan, Toyota Motor Corporation and Toyota Tsusho Corporation of Japan.
		 The Company manufactures and markets Toyota brand vehicles in Pakistan. The main product offerings include several
		 variants of the flagship ‘Corolla’ in the passenger cars category, ‘Hilux’ in the light commercial vehicles segment and
		 the ‘Fortuner’ Sports Utility Vehicle. The manufacturing facility and offices are located at a 105 acre site in 
		 Port Qasim, Karachi, while the product is delivered to end customers nationwide through a strong network of 45 independent 
		 3S Dealerships spread across the country.<p>
<br><br>
 

    <p>In its 27 years history since inception, IMC has sold more than 750,000 CBU/CKD vehicles and has demonstrated impressive growth, 
in terms of volumetric increase from a modest beginning of 20 vehicles per day production in 1993 to 240 units daily at present 
through the development of human talent embracing the ‘Toyota Way’ of quality and lean manufacturing.<p>

 <br><br>

  <p>Over the years, IMC has made large scale investments in enhancing its own capacity and in meeting customer requirements for
 new products. Today, Corolla is the largest selling automotive brand model in Pakistan and it also has the distinction of
 being # 1 in Toyota’s Asian market.<p>

 <br><br>

      <p>The Company invests heavily in training its 2,800 plus workforce of team members and management employees and creating a
 culture of high performing empowered teams working seamlessly across processes in search of quality and continuous improvement.<p>
